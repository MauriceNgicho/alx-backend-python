#!/usr/bin/env python3
"""
A module that provides a function to add two floats
"""


def add(a: float, b: float) -> float:
    """
    Adds two floats and return results.

    Args:
        a (float): The first float.
        b (float): The second float.

    Returns:
        float: The added fload of a and b.
    """
    return (a + b)
